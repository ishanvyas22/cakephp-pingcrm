<template>
    <div>
        <div class="mb-8 flex justify-start max-w-3xl">
            <h1 class="font-bold text-3xl">
                <inertia-link class="text-indigo-400 hover:text-indigo-600" href="/users">Users</inertia-link>
                <span class="text-indigo-400 font-medium">/</span>
                {{ form.first_name }} {{ form.last_name }}
            </h1>
            <img v-if="user.photo" class="block w-8 h-8 rounded-full ml-4" :src="user.photo" />
        </div>
        <trashed-message v-if="user.deleted_at" class="mb-6" @restore="restore">
            This user has been deleted.
        </trashed-message>
        <div class="bg-white rounded shadow overflow-hidden max-w-3xl">
            <form @submit.prevent="submit">
                <div class="p-8 -mr-6 -mb-8 flex flex-wrap">
                    <text-input
                        v-model="form.first_name"
                        :error="errors.first_name"
                        class="pr-6 pb-8 w-full lg:w-1/2"
                        label="First name"
                    />
                    <text-input
                        v-model="form.last_name"
                        :error="errors.last_name"
                        class="pr-6 pb-8 w-full lg:w-1/2"
                        label="Last name"
                    />
                    <text-input
                        v-model="form.email"
                        :error="errors.email"
                        class="pr-6 pb-8 w-full lg:w-1/2"
                        label="Email"
                    />
                    <text-input
                        v-model="form.password"
                        :error="errors.password"
                        class="pr-6 pb-8 w-full lg:w-1/2"
                        type="password"
                        autocomplete="new-password"
                        label="Password"
                    />
                    <select-input
                        v-model="form.owner"
                        :error="errors.owner"
                        class="pr-6 pb-8 w-full lg:w-1/2"
                        label="Owner"
                    >
                        <option :value="true">Yes</option>
                        <option :value="false">No</option>
                    </select-input>
                    <file-input
                        v-model="form.photo"
                        :error="errors.photo"
                        class="pr-6 pb-8 w-full lg:w-1/2"
                        type="file"
                        accept="image/*"
                        label="Photo"
                    />
                </div>
                <div class="px-8 py-4 bg-gray-100 border-t border-gray-200 flex items-center">
                    <button
                        v-if="!user.deleted_at"
                        class="text-red-600 hover:underline"
                        tabindex="-1"
                        type="button"
                        @click="destroy"
                    >
                        Delete User
                    </button>
                    <loading-button :loading="sending" class="btn-indigo ml-auto" type="submit"
                        >Update User</loading-button
                    >
                </div>
            </form>
        </div>
    </div>
</template>

<script>
import Layout from '@/Shared/Layout';
import LoadingButton from '@/Shared/LoadingButton';
import SelectInput from '@/Shared/SelectInput';
import TextInput from '@/Shared/TextInput';
import FileInput from '@/Shared/FileInput';
import TrashedMessage from '@/Shared/TrashedMessage';

export default {
    metaInfo() {
        return {
            title: `${this.form.first_name} ${this.form.last_name}`,
        };
    },
    layout: Layout,
    components: {
        LoadingButton,
        SelectInput,
        TextInput,
        FileInput,
        TrashedMessage,
    },
    props: {
        errors: Array,
        user: Object,
    },
    remember: 'form',
    data() {
        return {
            sending: false,
            form: {
                first_name: this.user.first_name,
                last_name: this.user.last_name,
                email: this.user.email,
                password: this.user.password,
                owner: this.user.owner,
                photo: null,
            },
        };
    },
    methods: {
        submit() {
            this.sending = true;

            var data = new FormData();
            data.append('first_name', this.form.first_name || '');
            data.append('last_name', this.form.last_name || '');
            data.append('email', this.form.email || '');
            data.append('password', this.form.password || '');
            data.append('owner', this.form.owner ? '1' : '0');
            data.append('photo', this.form.photo || '');
            data.append('_csrfToken', this.$page._csrfToken);

            this.$inertia.post(`/users/edit/${this.user.id}`, data).then(() => {
                this.sending = false;
                if (Object.keys(this.$page.errors).length === 0) {
                    this.form.photo = null;
                    this.form.password = null;
                }
            });
        },
        destroy() {
            if (confirm('Are you sure you want to delete this user?')) {
                this.$inertia.delete(`/users/delete/${this.user.id}`);
            }
        },
        restore() {
            if (confirm('Are you sure you want to restore this user?')) {
                this.$inertia.put(`/users/restore/${this.user.id}`);
            }
        },
    },
};
</script>
